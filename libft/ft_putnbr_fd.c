#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char	nr[10];
	int		s;
	int		i;

	if (n < 0)
	{
		s = -1;
		ft_putchar_fd('-', fd);
	}
	else
		s = 1;
	i = 0;
	while (n || i == 0)
	{
		nr[i++] = (n % 10) * s + 48;
		n /= 10;
	}
	while (--i >= 0)
		write(fd, &nr[i], 1);
}
