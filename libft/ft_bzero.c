#include "libft.h"

void	ft_bzero(void *str, size_t n)
{
	unsigned	char	*b;
	int					i;

	i = 0;
	b = (unsigned char*)str;
	while (n > 0)
	{
		*b = '\0';
		b++;
		n--;
	}
}
